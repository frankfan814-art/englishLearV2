def get_jlpt_level(word_entry: dict, index: int, total_words: int) -> str:
    """
    基于词频位置 + 词性 + 已知词汇表的启发式 JLPT 分级
    """
    word = word_entry.get('word', '')
    pos = word_entry.get('pos', '')
    phonetic = word_entry.get('phonetic', '')

    # 1. 先检查是否在已知词汇表中
    if word in N5_WORDS:
        return 'jlpt_n5'

    if word in N4_WORDS:
        return 'jlpt_n4'

    # 2. 基于词频位置（词汇在词库中的排序）
    ratio = index / total_words

    # 前 10% 的词 → N3
    if ratio < 0.1:
        return 'jlpt_n3'

    # 10%-30% → N2
    if ratio < 0.3:
        return 'jlpt_n2'

    # 30%+ → N1
    return 'jlpt_n1'


def tag_japanese_words():
    """为日语词库打 JLPT 标签"""
    data_dir = 'public/data/ja'
    total_words = 19929

    for i in range(1, 5):
        filepath = os.path.join(data_dir, f'words-{i:03d}.json')

        with open(filepath, encoding='utf-8') as f:
            data = json.load(f)

        tagged_count = 0
        for word in data['words']:
            # 跳过已有标签的词
            if word.get('tag') and word['tag'].strip():
                continue

            level = get_jlpt_level(word, word['id'] - 1, total_words)
            word['tag'] = level
            tagged_count += 1

        # 写回文件
        with open(filepath, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)

        # 统计
        tag_counts = {}
        for w in data['words']:
            t = w.get('tag', '')
            tag_counts[t] = tag_counts.get(t, 0) + 1

        print(f'Shard {i}: {tagged_count} words tagged, distribution: {tag_counts}')
        print(f'  Sample: {[w["word"] + "→" + w["tag"] for w in data["words"][:5]]}')

    print(f'\nDone! Total: {total_words} words tagged.')
    print('Distribution:')

    # 汇总
    all_tags = {}
    for i in range(1, 5):
        with open(os.path.join(data_dir, f'words-{i:03d}.json'), encoding='utf-8') as f:
            data = json.load(f)
            for w in data['words']:
                t = w.get('tag', '')
                all_tags[t] = all_tags.get(t, 0) + 1
    for tag, count in sorted(all_tags.items()):
        pct = count / total_words * 100
        print(f'  {tag}: {count} ({pct:.1f}%)')


if __name__ == '__main__':
    tag_japanese_words()